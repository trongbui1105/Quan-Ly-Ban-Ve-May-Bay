package Project;


public class SanBay {
	private String maSB, tenSB, quocGia;
	
	public SanBay() {
		this.maSB = "";
		this.tenSB = "";
		this.quocGia = "";
	}

	public SanBay(String maSB, String tenSB, String quocGia) {
		this.maSB = maSB;
		this.tenSB = tenSB;
		this.quocGia = quocGia;
	}

	public String getMaSB() {
		return maSB;
	}

	public void setMaSB(String maSB) {
		this.maSB = maSB;
	}

	public String getTenSB() {
		return tenSB;
	}

	public void setTenSB(String tenSB) {
		this.tenSB = tenSB;
	}

	public String getQuocGia() {
		return quocGia;
	}

	public void setQuocGia(String quocGia) {
		this.quocGia = quocGia;
	}
	
	
	


	
}
